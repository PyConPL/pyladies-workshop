# dzialania na liczbach
print('20+17=',20 + 17)
print('2 + 0 + 1 + 7=',20 + 17)
print('20 - 1 - 7=',20 - 1 - 7)
print('20 * 17=',20 * 17)
print('20 / 17=',20 / 17)


print('dzielenie bez reszty','20 // 17=',20 // 17)
print('Potegowanie: 201^7=',201 ** 7)
print('sprawdzić resztę z dzielenia (ang. modulo): 20/17=',20 % 17)
print('Łączenie działań: 20 / 2 + 17 * 3=',20 / 2 + 17 * 3)
print('Łączenie działań: 20 / (20 * (2 + 17)) / 3=',(20 * (2 + 17)) / 3)
print('Dzialania na liczbach rzeczywistych ("float"): 6.7 + 0.3 - 2.5=',6.7 + 0.3 - 2.5)

